let country = "Nigeria";

switch(country) {
    case "Norway":
        console.log(`${country} is in europe`);
        break;
    case "Japan":
        console.log(`${country} is in asia`);
        break;
    case "Nigeria":
        console.log(`${country} is in africa`);
        break;
    default:
        console.log('Not on the list');
        break;
}